frutas = ['laranja', 'pera', 'uva', 'abacaxi'] #lista
frutas.append('melão') #adiciona melão na lista
frutas.sort() #coloca em ordem alfabetica
frutas.reverse() #lê de trás para a frente
frutas.remove('pera') #remove pera
frutas.pop() #remove o ultimo ou o objeto da posição dentro do parenteses


print(frutas)
print(frutas[0])
print(frutas.clear()) #limpa a lista